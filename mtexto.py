texto = "python é incrivel!"

# maiusculas e minúsculas
print(texto.upper()) # PYTON É INCRÍVEL!
print(texto.lower()) # pyton é incrível!

# tamanho da string
print(len(texto)) # 18 caracteres (incluindo espaços e pontuação)

# Substituição
novo_texto = texto.replace("incrível", "poderoso")
print(novo_texto) # Python é poderoso!
